package br.edu.unifei.ecot12.trabalho.setor;

import br.edu.unifei.ecot12.trabalho.futebol.Setor;

public class Vestiario extends Setor {
	private int armario;
	private int espelho;
	private int chuveiro;
	private int privada;
	private int banco;
	private int quadroBranco;
	private int caneta;

	public int getArmario() {
		return armario;
	}

	public void setArmario(int armario) {
		this.armario = armario;
	}

	public int getEspelho() {
		return espelho;
	}

	public void setEspelho(int espelho) {
		this.espelho = espelho;
	}

	public int getChuveiro() {
		return chuveiro;
	}

	public void setChuveiro(int chuveiro) {
		this.chuveiro = chuveiro;
	}

	public int getPrivada() {
		return privada;
	}

	public void setPrivada(int privada) {
		this.privada = privada;
	}

	public int getBanco() {
		return banco;
	}

	public void setBanco(int banco) {
		this.banco = banco;
	}

	public int getQuadroBranco() {
		return quadroBranco;
	}

	public void setQuadroBranco(int quadroBranco) {
		this.quadroBranco = quadroBranco;
	}

	public int getCaneta() {
		return caneta;
	}

	public void setCaneta(int caneta) {
		this.caneta = caneta;
	}

}
